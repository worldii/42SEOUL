int	ft_str_is_numeric(char *str)
{
	int	idx;
	int	flag;

	idx = 0;
	flag = 1;
	while (str[idx] != '\0')
	{
		if (!('0' <= str[idx] && str[idx] <= '9'))
		{
			flag = 0 ;
		}
		idx++;
	}
	return (flag);
}
