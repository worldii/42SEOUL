int	ft_strcmp(char *s1, char *s2)
{
	int	s1idx;
	int	s2idx;

	s1idx = 0;
	s2idx = 0;
	while (s1[s1idx] != '\0' && s2[s2idx] != '\0')
	{
		if (s1[s1idx] != s2[s2idx])
		{
			return (s1[s1idx] - s2[s2idx]);
		}
		s1idx++;
		s2idx++;
	}
	return (s1[s1idx] - s2[s2idx]);
}
